using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Castle.MonoRail.Rest
{
    public static class ResponseFormatExtensions
    {
        public static void Xml(this ResponseFormat format, ResponderDelegate renderer)
        {
            ((ResponseFormatInternal)format).AddRenderer("xml", renderer);            
        }

        public static void Html(this ResponseFormat format, ResponderDelegate renderer)
        {
            ((ResponseFormatInternal)format).AddRenderer("html", renderer);
        }
    }
}
